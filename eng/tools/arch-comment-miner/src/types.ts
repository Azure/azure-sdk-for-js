/** A single comment within a review thread. */
export interface ThreadComment {
  author: string;
  body: string;
  createdAt: string;
  url: string;
}

/** A review thread on a review/*.api.md file. */
export interface ReviewThread {
  /** PR number. */
  pr: number;
  /** File path (e.g. sdk/identity/identity/review/identity.api.md). */
  path: string;
  /** Full diff hunk from GitHub. */
  diffHunk: string;
  /** The line in the diff that the comment targets (1-based, may be null). */
  line: number | null;
  /** The original (base) line in the diff (for comments on removed lines). */
  originalLine: number | null;
  /** Direct permalink to the thread. */
  threadUrl: string;
  /** Whether the thread was resolved (feedback acted upon). */
  isResolved: boolean;
  /** Whether the thread is outdated (code has changed since the comment). */
  isOutdated: boolean;
  /** All comments in the thread, chronologically ordered. */
  comments: ThreadComment[];
}

/** A review thread enriched with parsed context. */
export interface EnrichedThread extends ReviewThread {
  /** The diff hunk with the commented line highlighted. */
  highlightedHunk: string;
  /** The specific line the comment targets, extracted from the hunk. */
  targetLine: string;
}

/** An enriched thread with analysis metadata from the analyze pipeline. */
export interface AnalyzedThread extends EnrichedThread {
  /** Cluster ID assigned by HAC (-1 = noise/unclustered). */
  clusterId: number;
  /** Human-readable label for this cluster, generated by LLM. */
  clusterLabel: string;
  /** Thread URL of the canonical (earliest) thread this is a duplicate of, or null. */
  isDuplicateOf: string | null;
  /** The architecture/design rule this thread enforces, generated by LLM. */
  threadRule: string;
}

/** Cluster metadata produced by the labeling step. */
export interface ClusterInfo {
  id: number;
  label: string;
  size: number;
  threadUrls: string[];
}

/** CLI options parsed from command-line arguments. */
export interface MinerOptions {
  since: string;
  until: string;
  repoOwner: string;
  repoName: string;
  output: string;
}
