import { ApiVersionType } from "../../common/types.js";
import { IApiVersionTypeExtractor, IModelOnlyChecker } from "../../common/interfaces.js";
import {
  getApiVersionTypeFromRestClient,
  tryFindRestClientPath,
  getApiVersionTypeFromNpm,
  getApiVersionTypeFromMetadata,
} from "../../xlc/apiVersion/utils.js";
import { logger } from "../../utils/logger.js";
import { getNpmPackageName } from "../../common/utils.js";
import { isBetaVersion } from "../../utils/version.js";
import { checkDirectoryExistsInGithub, checkGitTagExists } from "../../common/npmUtils.js";
import { tryGetNpmView } from "../../common/npmUtils.js";
import {
  getLatestStableVersion,
  getNextBetaVersion,
  getUsedVersions,
} from "../../utils/version.js";

export const getApiVersionType: IApiVersionTypeExtractor = async (
  packageRoot: string,
  apiVersion?: string,
): Promise<ApiVersionType> => {
  if (apiVersion) {
    return isBetaVersion(apiVersion) ? ApiVersionType.Preview : ApiVersionType.Stable;
  }

  // Try reading from metadata.json (generated by codegen for Azure flavor packages)
  // Check this first as it's the primary source for API version information
  logger.info("Trying to get api version from metadata.json");
  const typeFromMetadata = await getApiVersionTypeFromMetadata(packageRoot);
  if (typeFromMetadata !== ApiVersionType.None) {
    return typeFromMetadata;
  }

  // NOTE: when there's customized code, emitter must put generated code in root/generated folder
  const clientPatterns = ["src/api/*Context.ts", "generated/api/*Context.ts"];
  for (const pattern of clientPatterns) {
    const typeFromClient = await getApiVersionTypeFromRestClient(
      packageRoot,
      pattern,
      tryFindRestClientPath,
    );
    if (typeFromClient !== ApiVersionType.None) return typeFromClient;
  }

  const isModelOnlyPackage = await isModelOnly(packageRoot);
  if (isModelOnlyPackage) {
    const packageName = getNpmPackageName(packageRoot);
    return await getApiVersionTypeFromNpm(packageName);
  }

  // Default to None if no version information found
  logger.warn(`Failed to determine API version type for ${packageRoot}, returning None`);
  return ApiVersionType.None;
};

/**
 * Resolve the npm version to use when looking up a git tag for model-only detection.
 *
 * Strategy:
 * 1. For beta releases, prefer the beta/next dist-tag version; otherwise use stable.
 * 2. Verify the chosen version has a real git tag locally.
 * 3. If not, fall back to the most recent npm-published version that does have a local git tag.
 *    e.g. if 3.0.0 tag is missing but 4.0.0-beta.1/2/3 exist, use 4.0.0-beta.3.
 */
function resolveReferenceVersion(
  packageName: string,
  npmViewResult: Record<string, unknown>,
  stableVersion: string,
  isBetaRelease?: boolean,
): string {
  const betaVersion = isBetaRelease ? getNextBetaVersion(npmViewResult) : undefined;
  const preferredVersion = betaVersion ?? stableVersion;
  logger.info(
    `[resolveReferenceVersion] preferred=${preferredVersion} (isBetaRelease=${isBetaRelease}, stableVersion=${stableVersion}, betaVersion=${betaVersion})`,
  );

  // Build candidate list: preferred version first, then all npm versions newest-first
  const allVersions = getUsedVersions(npmViewResult).reverse();
  const candidates = [preferredVersion, ...allVersions.filter((v) => v !== preferredVersion)];

  for (const v of candidates) {
    if (checkGitTagExists(`${packageName}_${v}`)) {
      if (v !== preferredVersion) {
        logger.info(
          `[resolveReferenceVersion] git tag ${packageName}_${preferredVersion} not found, falling back to ${v}`,
        );
      }
      return v;
    }
  }

  logger.warn(
    `[resolveReferenceVersion] no git tag found for any version of ${packageName}, using preferred version ${preferredVersion} as-is`,
  );
  return preferredVersion;
}

export const isModelOnly: IModelOnlyChecker = async (
  packageRoot: string,
  isBetaRelease?: boolean,
): Promise<boolean> => {
  // Get npm view to find the latest stable version
  const packageName = getNpmPackageName(packageRoot);
  const npmViewResult = await tryGetNpmView(packageName);
  if (!npmViewResult) {
    logger.warn(`No npm package found for ${packageName}, cannot check GitHub directory`);
    return true;
  }

  const stableVersion = getLatestStableVersion(npmViewResult);
  if (!stableVersion) {
    logger.warn(`No stable version found for ${packageName}, cannot check GitHub directory`);
    return true;
  }

  const referenceVersion = resolveReferenceVersion(
    packageName,
    npmViewResult,
    stableVersion,
    isBetaRelease,
  );

  // Check if src/api exists in the GitHub repository
  const hasOperationsInGithub = await checkDirectoryExistsInGithub(
    packageRoot,
    ["src/api", "src/operations"],
    packageName,
    referenceVersion,
  );

  if (!hasOperationsInGithub) {
    logger.warn(
      `Neither "src/api" nor "src/operations" found in GitHub for ${packageName}; treating as a model-only service.`,
    );
    return true;
  }

  logger.info(
    `Found one of "src/api" / "src/operations" in GitHub for ${packageRoot}; this is not a model-only service.`,
  );
  return false;
};
