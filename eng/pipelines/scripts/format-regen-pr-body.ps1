[CmdletBinding()]
param(
  [Parameter(Mandatory = $true)]
  [string]$AggPath,

  [Parameter(Mandatory = $true)]
  [string]$FailedLogPath,

  [Parameter(Mandatory = $true)]
  [string]$BuildDefinitionName,

  [Parameter(Mandatory = $true)]
  [string]$BuildNumber,

  [Parameter(Mandatory = $true)]
  [string]$BuildResultsUrl,

  [Parameter(Mandatory = $true)]
  [string]$EmitterVersion,

  [Parameter(Mandatory = $true)]
  [string]$ChangePushMode,

  [Parameter(Mandatory = $true)]
  [string]$PullRequestTargetBranch,

  [Parameter(Mandatory = $true)]
  [string]$OutputPath
)

$ErrorActionPreference = "Stop"
$lines = New-Object System.Collections.Generic.List[string]

$lines.Add("Generated by ``$BuildDefinitionName`` build [$BuildNumber]($BuildResultsUrl).")
$lines.Add("Emitter version: ``$EmitterVersion``. Push mode: ``$ChangePushMode``.")
$lines.Add("")

if (Test-Path $AggPath) {
  $agg = Get-Content $AggPath -Raw | ConvertFrom-Json

  $lines.Add("## Regeneration summary")
  $lines.Add("- Regenerated: **$($agg.regeneration.success)/$($agg.regeneration.total)**")
  $lines.Add("- Built OK:    **$($agg.build.success)/$($agg.build.total)**")
  $lines.Add("- Changelog generated: **$($agg.changelog.generated)/$($agg.changelog.total)**")
  $lines.Add("- Packages with breaking changes: **$($agg.changelog.withBreaking)**")
  $lines.Add("")

  if ($agg.changelog.breakingPackages -and $agg.changelog.breakingPackages.Count -gt 0) {
    $lines.Add("## Packages with breaking changes ($($agg.changelog.breakingPackages.Count))")
    $lines.Add("The following $($agg.changelog.breakingPackages.Count) package(s) had breaking changes detected by ``update-changelog``. This may indicate an emitter regression or an intentional spec change — open each CHANGELOG below to triage.")
    $lines.Add("")
    foreach ($packagePath in $agg.changelog.breakingPackages) {
      $pkgShort = ($packagePath -split "/")[-1]
      $lines.Add("- [``$pkgShort``]($packagePath/CHANGELOG.md) &nbsp;<sub>$packagePath</sub>")
    }
    $lines.Add("")
  }

  if ($agg.skippedNoTspLocation -and $agg.skippedNoTspLocation.Count -gt 0) {
    $lines.Add("## Packages skipped — no usable tsp-location.yaml ($($agg.skippedNoTspLocation.Count))")
    $lines.Add("These packages were filtered out by matrix gen (`-OnlyTypeSpec true`) and are NOT covered by this PR.")
    $lines.Add("**Spec / SDK team action**: please add a `tsp-location.yaml` (or fix the existing one) so these packages participate in the next regeneration run.")
    $lines.Add("")

    $reasonBuckets = @{}
    foreach ($skipped in $agg.skippedNoTspLocation) {
      $reason = $skipped.reason
      if ($reason -like "no tsp-location.yaml*") {
        $bucket = "no tsp-location.yaml"
      }
      elseif ($reason -like "stale path*") {
        $bucket = "stale path (tspconfig.yaml missing in spec)"
      }
      elseif ($reason -like "relative repo*") {
        $bucket = "relative repo (local-only tsp-location)"
      }
      else {
        $bucket = $reason
      }

      if (-not $reasonBuckets.ContainsKey($bucket)) {
        $reasonBuckets[$bucket] = 0
      }
      $reasonBuckets[$bucket]++
    }

    $lines.Add("**Breakdown by reason:**")
    foreach ($bucket in ($reasonBuckets.Keys | Sort-Object { -$reasonBuckets[$_] })) {
      $lines.Add("- $bucket — **$($reasonBuckets[$bucket])**")
    }
    $lines.Add("")
    $lines.Add("<details><summary>Click to expand the full list</summary>")
    $lines.Add("")
    foreach ($skipped in $agg.skippedNoTspLocation) {
      $lines.Add("- ``$($skipped.name)`` — $($skipped.reason)")
    }
    $lines.Add("")
    $lines.Add("</details>")
    $lines.Add("")
  }
}
else {
  $lines.Add("_(aggregated-results.json not found; falling back to minimal description)_")
}

if (Test-Path $FailedLogPath) {
  $failedEntries = Get-Content $FailedLogPath | Where-Object { $_ -and $_.Trim().Length -gt 0 }
  if ($failedEntries.Count -gt 0) {
    $lines.Add("## Patches skipped due to upstream conflicts ($($failedEntries.Count))")
    $lines.Add("These files were dropped because the PR target branch (``$PullRequestTargetBranch``) moved underneath the regeneration shard — typically an ``[AutoPR @azure-arm-*]`` commit landed in the same file between shard checkout and PR creation. They are **not** part of this PR; the next regeneration run will pick them up against the new baseline.")
    $lines.Add("")
    foreach ($entry in $failedEntries) {
      $lines.Add("- ``$entry``")
    }
    $lines.Add("")
  }
}

$outputDir = Split-Path $OutputPath -Parent
if ($outputDir) {
  New-Item -ItemType Directory -Force -Path $outputDir | Out-Null
}

$lines | Set-Content -Path $OutputPath -Encoding utf8

Write-Host "----- PR body preview (first 60 lines) -----"
$lines | Select-Object -First 60 | ForEach-Object { Write-Host $_ }
Write-Host "----- end preview -----"
Write-Host "PR body written to $OutputPath"
