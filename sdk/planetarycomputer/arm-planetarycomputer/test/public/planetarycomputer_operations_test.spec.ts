import type { Recorder } from "@azure-tools/test-recorder";
import { env, isPlaybackMode } from "@azure-tools/test-recorder";
import { createTestCredential } from "@azure-tools/test-credential";
import { assert, beforeEach, afterEach, it, describe } from "vitest";
import { createRecorder } from "./utils/recordedClient.js";
import { SpatioClient } from "../../src/spatioClient.js";

export const testPollingOptions = {
  updateIntervalInMs: isPlaybackMode() ? 0 : undefined,
};

describe("planetarycomputer test", () => {
  let recorder: Recorder;
  let subscriptionId: string;
  let client: SpatioClient;
  let resourceGroupName: string;
  let catalogName: string;

  beforeEach(async (context) => {
    process.env.SystemRoot = process.env.SystemRoot || "C:\\Windows";
    recorder = await createRecorder(context);
    subscriptionId = env.SUBSCRIPTION_ID || "";
    resourceGroupName = env.RESOURCE_GROUP || "";
    catalogName = env.CATALOG_NAME || "";
    const credential = createTestCredential();
    client = new SpatioClient(credential, subscriptionId, recorder.configureClientOptions({ apiVersion: "2026-04-15" }));
  });

  afterEach(async function () {
    await recorder.stop();
  });

  it("geoCatalogs create test", { timeout: 9000000 }, async function () {
    const poller = client.geoCatalogs.create(resourceGroupName, catalogName, {
      location: "eastus",
      properties: {
        autoGeneratedDomainNameLabelScope: "TenantReuse",
      },
    }, testPollingOptions);
    const result = await poller.pollUntilDone();
    assert.ok(result.name);
    assert.equal(result.properties?.provisioningState, "Succeeded");
  });

  it("geoCatalogs get test", async function () {
    const result = await client.geoCatalogs.get(resourceGroupName, catalogName);
    assert.ok(result.name);
    assert.ok(result.properties?.catalogUri);
  });

  it("geoCatalogs update test", { timeout: 9000000 }, async function () {
    const poller = client.geoCatalogs.update(resourceGroupName, catalogName, {
      tags: { testKey: "testValue" },
    }, testPollingOptions);
    await poller.pollUntilDone();
    const result = await client.geoCatalogs.get(resourceGroupName, catalogName);
    assert.equal(result.tags?.["testKey"], "testValue");
  });

  it("geoCatalogs list by resource group test", async function () {
    const resArray = new Array();
    for await (const item of client.geoCatalogs.listByResourceGroup(resourceGroupName)) {
      resArray.push(item);
    }
    assert.ok(resArray.length >= 1);
  });

  it("geoCatalogs list by subscription test", async function () {
    const resArray = new Array();
    for await (const item of client.geoCatalogs.listBySubscription()) {
      resArray.push(item);
    }
    assert.ok(resArray.length >= 1);
  });

  it("geoCatalogs delete test", { timeout: 9000000 }, async function () {
    const poller = client.geoCatalogs.delete(resourceGroupName, catalogName, testPollingOptions);
    await poller.pollUntilDone();
  });
});
