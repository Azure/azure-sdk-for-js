const { SpatioClient } = require("@azure/arm-planetarycomputer");
const { DefaultAzureCredential } = require("@azure/identity");

/**
 * This sample demonstrates how to create a GeoCatalog
 *
 * @summary create a GeoCatalog
 * x-ms-original-file: 2025-02-11-preview/GeoCatalogs_Create.json
 */
async function geoCatalogsCreate() {
  const credential = new DefaultAzureCredential();
  const subscriptionId = "cd9b6cdf-dcf0-4dca-ab19-82be07b74704";
  const client = new SpatioClient(credential, subscriptionId);
  const result = await client.geoCatalogs.create("MyResourceGroup", "MyCatalog", {
    properties: {
      tier: "Basic",
      autoGeneratedDomainNameLabelScope: "NoReuse",
    },
    identity: {
      type: "UserAssigned",
      userAssignedIdentities: {
        "/subscriptions/cd9b6cdf-dcf0-4dca-ab19-82be07b74704/resourceGroups/MyResourceGroup/providers/Microsoft.ManagedIdentity/userAssignedIdentities/MyManagedIdentity":
          {},
      },
    },
    tags: { MyTag: "MyValue" },
    location: "eastus",
  });
  console.log(result);
}

async function main() {
  await geoCatalogsCreate();
}

main().catch(console.error);
