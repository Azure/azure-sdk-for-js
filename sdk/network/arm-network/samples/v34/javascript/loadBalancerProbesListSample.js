const { NetworkManagementClient } = require("@azure/arm-network");
const { DefaultAzureCredential } = require("@azure/identity");
require("dotenv/config");

/**
 * This sample demonstrates how to Gets all the load balancer probes.
 *
 * @summary Gets all the load balancer probes.
 * x-ms-original-file: specification/network/resource-manager/Microsoft.Network/stable/2025-01-01/examples/LoadBalancerProbeList.json
 */
async function loadBalancerProbeList() {
  const subscriptionId = process.env["NETWORK_SUBSCRIPTION_ID"] || "subid";
  const resourceGroupName = process.env["NETWORK_RESOURCE_GROUP"] || "testrg";
  const loadBalancerName = "lb";
  const credential = new DefaultAzureCredential();
  const client = new NetworkManagementClient(credential, subscriptionId);
  const resArray = new Array();
  for await (const item of client.loadBalancerProbes.list(resourceGroupName, loadBalancerName)) {
    resArray.push(item);
  }
  console.log(resArray);
}

async function main() {
  await loadBalancerProbeList();
}

main().catch(console.error);
