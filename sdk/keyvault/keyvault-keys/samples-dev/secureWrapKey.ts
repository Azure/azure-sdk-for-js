/**
 * @summary Wraps a TEE-generated symmetric key with a Managed HSM key, then unwraps it.
 *
 * Demonstrates the SECURE WRAP and SECURE UNWRAP operations on Azure Managed
 * HSM. Unlike the regular wrap/unwrap operations, the symmetric key being
 * wrapped is generated inside a Trusted Execution Environment (TEE), and the
 * unwrap operation is gated by attestation through Microsoft Azure
 * Attestation (MAA).
 *
 * These operations are only available on Managed HSM. The wrapping key must
 * be exportable and have a release policy that trusts the attestation
 * authority.
 *
 * Required environment variables:
 *  - `AZURE_MANAGEDHSM_URI`           e.g. https://my-mhsm.managedhsm.azure.net
 *  - `AZURE_KEYVAULT_ATTESTATION_URI` MAA endpoint trusted by the HSM
 *
 * The MAA endpoint is expected to expose a `/generate-test-token` route that
 * returns `{ "token": "<jwt>" }` (this matches the
 * `keyvault-mock-attestation` service used by Key Vault SDK tests).
 */

import { KeyClient } from "@azure/keyvault-keys";
import { DefaultAzureCredential } from "@azure/identity";
import "dotenv/config";

async function fetchAttestationToken(attestationUri: string): Promise<string> {
  const response = await fetch(`${attestationUri.replace(/\/$/, "")}/generate-test-token`);
  if (!response.ok) {
    throw new Error(
      `Failed to fetch attestation token from ${attestationUri}: ${response.status} ${response.statusText}`,
    );
  }
  const body = (await response.json()) as { token: string };
  return body.token;
}

export async function main(): Promise<void> {
  const url = process.env["AZURE_MANAGEDHSM_URI"];
  const attestationUri = process.env["AZURE_KEYVAULT_ATTESTATION_URI"];
  if (!url || !attestationUri) {
    throw new Error("AZURE_MANAGEDHSM_URI and AZURE_KEYVAULT_ATTESTATION_URI must be set.");
  }

  const credential = new DefaultAzureCredential();
  const client = new KeyClient(url, credential);

  // Create an HSM-protected RSA key authorized for secure wrap/unwrap.
  // The key must NOT be exportable, but it MUST carry a release policy
  // identifying the attestation authority that gates the unwrap operation.
  const keyName = `secure-wrap-sample-${Date.now()}`;
  const releasePolicy = {
    anyOf: [
      {
        allOf: [{ claim: "sdk-test", equals: "true" }],
        authority: attestationUri,
      },
    ],
    version: "1.0.0",
  };

  await client.createRsaKey(keyName, {
    hsm: true,
    keyOps: ["secureWrapKey", "secureUnwrapKey"],
    releasePolicy: {
      encodedPolicy: new TextEncoder().encode(JSON.stringify(releasePolicy)),
    },
  });
  console.log(`Created key: ${keyName}`);

  // 1) Securely wrap a symmetric key generated by the HSM/TEE.
  const wrapped = await client.secureWrapKey(keyName, "RSA-OAEP-256");
  console.log(`Wrapped key (${wrapped.result.byteLength} bytes) using ${wrapped.algorithm}`);

  // 2) Fetch an attestation token and unwrap.
  const attestationToken = await fetchAttestationToken(attestationUri);
  const unwrapped = await client.secureUnwrapKey(
    keyName,
    wrapped.algorithm,
    wrapped.result,
    attestationToken,
  );
  console.log(`Unwrapped key (${unwrapped.result.byteLength} bytes) using ${unwrapped.algorithm}`);
}

main().catch((error) => {
  console.error("An error occurred:", error);
  process.exit(1);
});
