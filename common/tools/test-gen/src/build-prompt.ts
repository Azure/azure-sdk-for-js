/**
 * build-prompt.ts
 *
 * Assembles a complete agent prompt for test generation by combining:
 * annotated source, context files, existing tests, conventions, and instructions.
 */

import { resolve } from "node:path";
import type { CoverageGap } from "./types.ts";
import { annotateSource, commentPrefixFor } from "./annotate-source.ts";
import type { ContextFile } from "./resolve-context.ts";
import { extractConventions } from "./extract-conventions.ts";
import { tryReadFile } from "./utils.ts";
import { defaults, codeFenceFor } from "./config.ts";
import type { ExamplesConfig, LanguageConfig } from "./config.ts";

export interface PromptContext {
  /** Absolute paths to existing spec files (used for conventions fallback). */
  specFiles: string[];
  examples?: ExamplesConfig;
  /** Language settings for prompts and code fences. */
  language?: LanguageConfig;
  /** Test directory name (e.g., "test") for output path instructions. */
  testDir?: string;
  /**
   * Pre-computed conventions document from LLM analysis of the test suite.
   * When provided, replaces the inline `extractConventions` call.
   */
  conventions?: string;
  /** Context files identified by LLM context resolution. */
  contextFiles?: ContextFile[];
  /** Content of existing test files that exercise the source under test. */
  existingTests?: string;
  /** Batch of gaps to annotate inline. Must be non-empty. */
  gapBatch: CoverageGap[];
  /** Batch index (used for logging; no longer affects filename). */
  batchIndex?: number;
  /**
   * Test functions already generated by earlier batches for this same source file.
   * Format: `["path/to/test.py::test_func_name", ...]`
   * Used to prevent duplication across batches.
   */
  priorBatchTests?: string[];
  /**
   * Fixture/helper files from the test directory (e.g., conftest.py, helpers.py).
   * Included directly so the LLM sees the exact fixture API surface.
   */
  fixtureFiles?: { path: string; content: string }[];
}

/**
 * Build the test generation prompt.
 *
 * Uses annotated source with inline ⚠️ markers, includes context files and
 * existing tests, and instructs the LLM to write exactly one test per marker.
 */
export async function buildPrompt(
  packageDir: string,
  sourceFile: string,
  ctx: PromptContext,
): Promise<string> {
  const examples = ctx.examples ?? defaults.examples;
  const lang = ctx.language ?? defaults.language;
  const testDir = ctx.testDir ?? defaults.paths.testDir;
  const codeFence = codeFenceFor(lang.outputExtension);

  const [sourceCode, conventions] = await Promise.all([
    tryReadFile(resolve(packageDir, sourceFile)),
    ctx.conventions
      ? Promise.resolve(ctx.conventions)
      : extractConventions(packageDir, ctx.specFiles, {
          count: examples.count,
          maxLines: examples.maxLines,
          language: lang,
        }),
  ]);

  if (!sourceCode) throw new Error(`Source file not found: ${resolve(packageDir, sourceFile)}`);

  if (!ctx.gapBatch || ctx.gapBatch.length === 0) {
    throw new Error("buildPrompt requires ctx.gapBatch to be non-empty");
  }

  return buildSinglePassPrompt(sourceFile, sourceCode, ctx, conventions, codeFence, lang, testDir);
}

function buildSinglePassPrompt(
  sourceFile: string,
  sourceCode: string,
  ctx: PromptContext,
  conventions: string | undefined,
  codeFence: string,
  lang: LanguageConfig,
  testDir: string,
): string {
  const gaps = ctx.gapBatch!;
  const batchSize = gaps.length;
  const comment = commentPrefixFor(lang.outputExtension);
  const annotated = annotateSource(sourceCode, gaps, comment);

  // Derive output filename stem from source path
  const stem = sourceFile
    .split("/")
    .pop()!
    .replace(/\.[^.]+$/, "")
    .replace(/^_+/, "");

  const sections: string[] = [];

  // 1. System preamble
  sections.push(
    `You are writing ${lang.testFramework} tests to cover specific uncovered branches in a source file.`,
  );

  // 2. Annotated source
  sections.push(
    `## Source Under Test

**File:** \`${sourceFile}\`

\`\`\`${codeFence}
${annotated}
\`\`\`

Each line marked ⚠️ UNCOVERED BRANCH is a branch that has zero test coverage.
Your job is to write exactly ONE test per ⚠️ marker — no more, no fewer.`,
  );

  // 3. Context files (from LLM resolution)
  if (ctx.contextFiles && ctx.contextFiles.length > 0) {
    const contextSections = ctx.contextFiles.map(
      (f) => `### \`${f.path}\` — ${f.reason}\n\n\`\`\`${codeFence}\n${f.content}\n\`\`\``,
    );
    sections.push(
      `## Context Files

These files were identified as necessary context for understanding the source under test.

${contextSections.join("\n\n")}`,
    );
  }

  // 4. Existing tests (from coverage DB or convention)
  if (ctx.existingTests) {
    sections.push(
      `## Existing Tests for This File

These tests already exist and exercise other branches of the same source file.
Study them to understand: how the code is instantiated, what fixtures are used,
what the assertion patterns look like. Do NOT duplicate any of these tests.

\`\`\`${codeFence}
${ctx.existingTests}
\`\`\``,
    );
  }

  // 5. Fixture/helper files (conftest.py, helpers, etc.)
  if (ctx.fixtureFiles && ctx.fixtureFiles.length > 0) {
    const fixtureSections = ctx.fixtureFiles.map(
      (f) => `### \`${f.path}\`\n\n\`\`\`${codeFence}\n${f.content}\n\`\`\``,
    );
    sections.push(
      `## Test Fixtures & Helpers

These files define the shared fixtures, helpers, and configuration available to all tests.
Use these fixtures instead of creating ad-hoc mocks or manual setup.

${fixtureSections.join("\n\n")}`,
    );
  }

  // 6. Conventions
  if (conventions) {
    sections.push(conventions);
  }

  // 6. Prior batch tests (to prevent cross-batch duplication)
  if (ctx.priorBatchTests && ctx.priorBatchTests.length > 0) {
    const testList = ctx.priorBatchTests.map((t) => `- \`${t}\``).join("\n");
    sections.push(
      `## Tests Already Generated (earlier batches)

The following tests were already generated for this same source file by earlier batches.
Do NOT write tests that duplicate any of these — skip any ⚠️ marker that is already covered.

${testList}`,
    );
  }

  // 7. Instructions
  sections.push(
    `## Instructions

For EACH ⚠️ UNCOVERED BRANCH marker in the source:

**Step 1 — Analyze the branch.** Read the \`if\`/\`elif\`/\`else\`/\`try\`/\`except\`/\`match\` statement.
Determine what condition must be true (or false) to enter the uncovered path. Identify what
inputs, state, or mocks would trigger that condition.

**Step 2 — Write the test.** Create a test function that:
- Sets up the EXACT precondition identified in Step 1
- Calls the function/method containing the uncovered branch
- Asserts the SPECIFIC outcome of taking that branch (return value, exception, side effect)

### Rules

1. Write EXACTLY ${batchSize} test functions — one per ⚠️ marker, in the same order they appear in the source.
2. Each test function name describes what branch it covers: \`test_<method>_when_<condition>_<expected_outcome>\`.
3. Every assertion checks a CONCRETE value. Never: \`assert result is not None\`. Always: \`assert result == expected_value\` or \`assert isinstance(result, SpecificType)\`.
4. If a branch raises an exception, test with \`pytest.raises(ExactExceptionType)\` (or framework equivalent) and verify the error message.
5. Import ONLY from the package's public API and test utilities — never from internal/private modules unless the existing tests show that pattern.
6. Follow the setup/teardown pattern shown in the existing tests and conventions — use the SAME fixtures, base classes, and decorators.
7. If a ⚠️ branch is unreachable with public API alone (e.g., defensive coding), write the test anyway using minimal mocking, and add a comment: \`${comment} Tests defensive branch — requires mock\`.
8. Do NOT duplicate functionality of any test in the "Existing Tests" section.

## Output

Respond with ONLY valid JSON — no markdown fences, no explanation:

{
  "path": "${testDir}/test_${stem}_gaps.${lang.outputExtension.replace(".", "")}",
  "code": "... complete test file content ...",
  "analysis": [
    {
      "marker_line": 142,
      "branch_condition": "description of what triggers this branch",
      "test_function": "test_method_when_condition_expected",
      "trigger_strategy": "how to set up inputs to trigger the branch"
    }
  ]
}

The "analysis" array must have exactly ${batchSize} entries matching the ⚠️ markers.
The "path" must start with \`${testDir}/\` and end with \`${lang.outputExtension}\`.
The "code" must be the complete, runnable test file.`,
  );

  return sections.filter(Boolean).join("\n\n");
}
